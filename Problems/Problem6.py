a = 100
sumOfSquare = (a * (a + 1) * (2 * a + 1)) / 6  # Sum of the Squares formula
squareOfSum = ((a / 2) * (1 + a))**2  # Gauss sum formula**2
print(squareOfSum-sumOfSquare)
